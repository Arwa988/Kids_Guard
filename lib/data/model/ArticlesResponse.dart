class ArticlesResponse {
  List<Data>? dataList;
  String? statusMsg;
  String? message;

  ArticlesResponse({this.dataList, this.statusMsg, this.message});

  ArticlesResponse.fromJson(dynamic json) {
    if (json is List) {
      dataList = json.map((v) => Data.fromJson(v)).toList();
    } else if (json is Map<String, dynamic>) {
      final dataKey = json["data"];
      if (dataKey is List) {
        dataList = dataKey.map((v) => Data.fromJson(v)).toList();
      }
    }
  }
}
